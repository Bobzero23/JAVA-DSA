package arrays;

/*Demonstration on how to traverse an Array*/

/*main class*/
public class Traversal1 {

    /*main method*/
    public static void main(String[] args) {

        /*initializing an array*/
        int[] arr = {1, 2, 3, 4, 5, 6, 7, 8};

        /*traversing an Array*/
        for (int i = 0; i < arr.length; i++){
            System.out.print(arr[i] + ", ");
        }
    }
}
